static inline int ui_font__strlen(const char* s) {
    if (!s) return 0;
    int n = 0;
    while (s[n]) n++;
    return n;
}

void ui_font16_draw_char(int px, int py, char c, uint32_t fg, uint32_t bg,
                         bool transparent_bg) {
    if (!framebuffer_available()) return;
    uint8_t idx = (uint8_t)c;
    if (idx < UI_FONT16_FIRST || idx > UI_FONT16_LAST) idx = '?';
    int glyph = idx - UI_FONT16_FIRST;

    for (int row = 0; row < UI_FONT16_H; row++) {
        uint16_t bits = ui_font16[glyph][row];
        for (int col = 0; col < UI_FONT16_W; col++) {
            bool lit = (bits & (0x8000u >> col)) != 0;
            if (lit) {
                put_pixel(px + col, py + row, fg);
            } else if (!transparent_bg) {
                put_pixel(px + col, py + row, bg);
            }
        }
    }
}

void ui_font16_print(int x, int y, const char* str, uint32_t fg, uint32_t bg,
                     bool transparent_bg) {
    if (!str) return;
    int cx = x;
    while (*str) {
        char c = *str++;
        if (c == '\n') {
            cx = x;
            y += UI_FONT16_H + 2;
            continue;
        }
        ui_font16_draw_char(cx, y, c, fg, bg, transparent_bg);
        cx += UI_FONT16_W;
    }
}

void ui_font16_print_centered(int x, int y, int area_w, const char* str,
                              uint32_t fg, uint32_t bg, bool transparent_bg) {
    if (!str) return;
    int text_w = ui_font16_text_width(str);
    int tx = x + (area_w - text_w) / 2;
    if (tx < x) tx = x;
    ui_font16_print(tx, y, str, fg, bg, transparent_bg);
}

int ui_font16_text_width(const char* str) {
    return ui_font__strlen(str) * UI_FONT16_W;
}

void ui_font16_print_ellipsis(int x, int y, const char* str, uint32_t fg,
                              uint32_t bg, bool transparent_bg, int max_pixels) {
    if (!str) return;
    int max_chars = max_pixels / UI_FONT16_W;
    int len = ui_font__strlen(str);
    if (len <= max_chars) {
        ui_font16_print(x, y, str, fg, bg, transparent_bg);
        return;
    }
    char buf[128];
    int copy = max_chars - 3;
    if (copy < 0) copy = 0;
    if (copy > 124) copy = 124;
    int i;
    for (i = 0; i < copy; i++) buf[i] = str[i];
    buf[i++] = '.'; buf[i++] = '.'; buf[i++] = '.'; buf[i] = '\0';
    ui_font16_print(x, y, buf, fg, bg, transparent_bg);
}